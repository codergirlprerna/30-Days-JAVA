//Arithmetic Operators
package Day2;

public class Activity1 {
    public static void main(String[] args) {
        //task1:Add two integers and print the result.
        int a=3;
        int b=4;
        System.out.println("Sum of two number " + a + " and " + b + " :" + (a+b));

        //task2:Subtract two doubles and print the result.
        int c=6;
        int d=2;
        System.out.println("Subtract of two number " + c + " and " + d + " :" +(c-d));

        //task3:Multiply two integers and print the product.
        int e=2;
        int f=3;
        System.out.println("Multiplication of two number " + e + " and " + f + " :" +(e*f));
    }
}
