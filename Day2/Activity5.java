//Increment operator

package Day2;

public class Activity5 {
    public static void main(String[] args) {
        //task9:Demonstrate pre-increment an integer and print it immediately.
        int a=5;
        System.out.println(++a);

        
        //task10:Demonstrate post-increment print an integer then increment it and print again.
        int b=4;
        System.out.println("Value of b is: " + b);
        System.out.println(b++);
        System.out.println("Value of b is: " + b);

    }
}
