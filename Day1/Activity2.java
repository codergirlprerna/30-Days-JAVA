//Activity2:Working with booleans and characters

package Day1;

public class Activity2 {
    public static void main(String[] args) {
        //task3:declare a boolean variable prints it value and toggle it and print again.

        boolean isAvailable = true;
        System.out.println("Initial Value : " + isAvailable);
        isAvailable=!isAvailable;
        System.out.println("Toggled Value : " + isAvailable);

        //task4:declare a char variable to store a single letter and print it.

        char ch = 'A';
        System.out.println("Character is : " + ch);

    }
}
